<p>
    Requestor will supply needed information to enable fiscal staff to pay an external vendor.
</p>

<section>
    <h3>Invoice Project Title</h3>

    <p>
        Standardize title this way: Invoice: [Budget] / [Vendor] / [1-2 word item description].
    </p>

</section>

<section>
    <h3>Requestor</h3>

    <p>
        Generally, this is you. If you are entering a request for reimbursement on behalf of someone else, change this to the appropriate person.
    </p>
</section>

<section>
    <h3>Description and Business Purpose </h3>

    <p>
        Enter a description of the goods/services purchased as well as an explanation of the business purpose
         (why the purchase was justified as an expenditure on this particular budget). Examples include:
    </p>

    <ul>
        <li>Dues for the Reinvention Collaborative, a consortium of research universities with expertise in the theory and practice of undergraduate education, for the academic year 2021-2022.</li>
        <li>Payment for five AmeriCorps workers serving Dream Project and Riverways for the academic year 2021-2022.</li>
        <li>Sponsorship of 12/1/21 John Doe lecture on Diversity in Higher Education through Seattle Arts & Lectures as part of equity and diversity initiatives.</li>
    </ul>
</section>
