<h3 class="mt-4">OneDrive</h3>

<p>
    To make managing files easier for you and the fiscal team we are storing files related
    to TREQ in a OneDrive file share. This gives you drag-and-drop file management,
    ability to preview files online, and to share files with your team.
</p>

<p>
    The fiscal office will designate a specific OneDrive folder you should use for your
    TREQ requests. A link will be provided to that folder within your TREQ Projects and
    Orders.
</p>

<p>
    If you don't have a TREQ OneDrive folder yet, contact your budget manager to get
    set up.
</p>

<h3 class="mt-4">Project Folder Name</h3>

<p>
    Your budget manager will give you instructions for how to organize your TREQ
    attachments in OneDrive. Generally they want you to create a folder per TREQ
    project, using the Project Number as the folder name. TREQ Project Numbers
    look like "TREQ001234".
</p>

<p>
    These folder names are just suggestions. It is up to you to create the folder in
    OneDrive. If your project has its own organization, understood by your fiscal budget
    manager, you should continue to use that.
</p>
