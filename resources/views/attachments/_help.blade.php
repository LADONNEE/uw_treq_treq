<h3 class="mt-4">OneDrive</h3>

<p>
    To make managing files easier for you and the fiscal team, we are storing files related 
    to TREQ in a OneDrive file share. This gives you drag-and-drop file management, 
    ability to preview files online and to share files with your team. 
</p>

<p>
    The fiscal office has designated a specific OneDrive folder you should use for your 
    TREQ requests. Use the link on this page to access your folder where you will create a unique sub-folder for each specific Project.
</p>

<p>
    Create one folder per TREQ Project using the Project Number as the folder name, e.g. TREQ001234. 
    If you don't have a TREQ OneDrive folder yet, contact your financial manager to get set up.
</p>

